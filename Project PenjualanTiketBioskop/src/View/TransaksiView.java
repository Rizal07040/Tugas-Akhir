        private void showTableview() throws SQLException {
        DefaultTableModel dt = new DefaultTableModel(new String[]{"NAMA_PEMBELI","JUDUL_FILM","HARGA_TIKET",
            "TGL_TRANSAKSI","HARGA_TOTAL"
        }, 0);
        dt.setRowCount(0);

        for (Detail_pemesanan dp : this.trans.getDataPemesanan()) {
            dt.addRow(new String[]{
                dp.getPembeli().getNama_pembeli(),
                dp.getPembelian().getJudul_film(),
                dp.getPembelian().getHarga_tiket().toString(),
                dp.getTgl_transaksi().toString(), 
                dp.getHarga_total().toString()
                
            });
        }
        this.tbl_transaksi.setModel(dt);
    }
