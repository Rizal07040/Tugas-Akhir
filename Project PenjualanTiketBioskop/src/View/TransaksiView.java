    public void showComboBoxFilm() throws SQLException{
        
        DefaultComboBoxModel f = new DefaultComboBoxModel();
            for (Pembelian pembelian :this.trans.getDataPembelian()){
                f.addElement(pembelian.getJudul_film());
            }
            this.cb_judul.setModel(f);
    }
    public void showComboBoxPembeli() throws SQLException{
        
        DefaultComboBoxModel p = new DefaultComboBoxModel();
            for (Pembeli pembeli :this.trans.getDataPembeli()){
                p.addElement(pembeli.getNama_pembeli());
            }
            this.cb_nama.setModel(p);
    }
    public void showComboBoxHarga() throws SQLException{
        
        DefaultComboBoxModel h = new DefaultComboBoxModel();
            for (Pembelian pembelian :this.trans.getDataPembelian()){
                h.addElement(pembelian.getHarga_tiket());
            }
            this.cb_harga.setModel(h);
    }
