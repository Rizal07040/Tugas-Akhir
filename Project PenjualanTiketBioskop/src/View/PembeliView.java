    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        Pembeli p = new Pembeli();
        try {
            p.setId_pembeli(Integer.parseInt(txt_id_pembeli.getText()));
            p.setNama_pembeli(txt_namapembeli.getText());
            p.setUmur(txt_umur.getText());
            p.setAlamat(txt_alamat.getText());
            p.setJenis_kelamin((String) cb_gender.getSelectedItem());
            this.trans.insertpembeli(p);
            this.showTablePembeli();
            this.showComboBoxGender();
            kosong();
        } catch (SQLException err) {
            System.out.println(err);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

