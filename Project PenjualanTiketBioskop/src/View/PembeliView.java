    private void jToggleButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jToggleButton1MouseClicked
        // TODO add your handling code here:
         DefaultTableModel dataModel = (DefaultTableModel) tbl_insert.getModel();
        List list = new ArrayList<>();
        tbl_insert.setAutoCreateColumnsFromModel(true);
        list.add (txt_id_pembeli.getText());
        list.add (txt_namapembeli.getText());
        list.add (txt_umur.getText());
        list.add (txt_alamat.getText());
        list.add (cb_gender.getSelectedItem());
        dataModel.addRow(list.toArray());
    }//GEN-LAST:event_jToggleButton1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel Model = (DefaultTableModel) tbl_insert.getModel();
        int i = tbl_insert.getSelectedRow();
        Integer p = Integer.parseInt(Model.getValueAt(i, 0).toString());
        try {
            this.trans.deletepembeli(p);
            this.showTablePembeli();
        } catch (SQLException ex) {
            Logger.getLogger(PembeliView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

