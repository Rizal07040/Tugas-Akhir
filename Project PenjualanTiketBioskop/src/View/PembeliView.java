    public void showComboBoxGender() throws SQLException{
               DefaultComboBoxModel g = new DefaultComboBoxModel();
            for (Pembeli pembeli :this.trans.getDataPembeli()){
                g.addElement(pembeli.getJenis_kelamin());
            }
            this.cb_gender.setModel(g);
    }
            private void showTablePembeli() throws SQLException {
        DefaultTableModel dtmFilm = new DefaultTableModel(new String[]{
            "ID_PEMBELI", "NAMA_PEMBELI", "UMUR", "ALAMAT","JENIS_KELAMIN"
        }, 0);
        dtmFilm.setRowCount(0);

        for (Pembeli dp : this.trans.getDataPembeli()) {
            dtmFilm.addRow(new String[]{
                dp.getId_pembeli().toString(), 
                dp.getNama_pembeli(),
                dp.getUmur(), 
                dp.getAlamat(),
                dp.getJenis_kelamin()
            });
        }
        this.tbl_insert.setModel(dtmFilm);
    }
