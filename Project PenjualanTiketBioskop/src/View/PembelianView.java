    public void showComboBoxNamapembeli() throws SQLException{
        
        DefaultComboBoxModel id = new DefaultComboBoxModel();
            for (Pembeli pembeli :this.trans.getDataPembeli()){
                id.addElement(pembeli.getId_pembeli());
            }
            this.cb_idpembeli.setModel(id);
    }
    
    public void showComboBoxFilm() throws SQLException{
        
        DefaultComboBoxModel f = new DefaultComboBoxModel();
            for (Pembelian pembelian :this.trans.getDataPembelian()){
                f.addElement(pembelian.getJudul_film());
            }
            this.cb_film.setModel(f);
    }

    public void showComboBoxJamTayang() throws SQLException{
        
        DefaultComboBoxModel j = new DefaultComboBoxModel();
            for (Pembelian pembelian :this.trans.getDataPembelian()){
                j.addElement(pembelian.getJam_tayang());
            }
            this.cb_jamtayang.setModel(j);
    }   
