            private void showTablePembelian() throws SQLException {
        DefaultTableModel dtmFilm = new DefaultTableModel(new String[]{
            "KODE TIKET","ID PEMBELI","JUDUL FILM", "NO KURSI", "JAM TAYANG","HARGA TIKET"
        }, 0);
        dtmFilm.setRowCount(0);

        for (Pembelian pp : this.trans.getDataPembelian()) {
            dtmFilm.addRow(new String[]{
                pp.getKode_tiket().toString(),
                pp.getPembeli().getId_pembeli().toString(), 
                pp.getJudul_film(),
                pp.getNo_kursi().toString(), 
                pp.getJam_tayang(),
                pp.getHarga_tiket().toString()
            });
        }
        this.txt_insert.setModel(dtmFilm);
    }

